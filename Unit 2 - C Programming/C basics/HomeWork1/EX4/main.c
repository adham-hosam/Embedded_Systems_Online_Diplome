//C Program to Multiply two Floating Point Numbers
#include <stdio.h>

int main() {
    float x,y;
    printf("Please enter the first number:");
    scanf("%f" ,&x );
    printf("Please enter the second number:");
    scanf("%f" ,&y );

    printf("the summation of %f and %f is:%f",x,y,x*y);
    return 0;
}
